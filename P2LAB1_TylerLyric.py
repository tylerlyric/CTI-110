# Python file that gives the radius of a circle
# then calculates the diameter, circumference, and area.

r = float(input("Enter the radius of a given circle: "))

d = 2 * r
c = 2 * 3.14 * r
a = 3.14 * r ** 2

print("The diameter of the circle is ", d)
print("The circumference of the circle is ", c)
print("The area of the circle is ", a)
